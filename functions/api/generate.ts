export const onRequestPost: PagesFunction<{ AI: Ai }> = async (context) => {
  try {
    const body = await context.request.json() as { slug?: string; input?: string };
    const slug = body.slug || 'default';
    const input = body.input || '';

    if (!input.trim()) {
      return new Response(JSON.stringify({ success: false, error: '请输入主题' }), {
        status: 400,
        headers: { 'Content-Type': 'application/json', ...CORS_HEADERS },
      });
    }

    const systemPrompt = NICHE_SYSTEM_PROMPTS[slug] || DEFAULT_SYSTEM_PROMPT;

    const response = await context.env.AI.run('@cf/meta/llama-3.2-3b-instruct', {
      messages: [
        { role: 'system', content: systemPrompt },
        { role: 'user', content: `主题：「${input}」\n\n请直接输出结果，不要有任何前缀说明。` },
      ],
      max_tokens: 1500,
      temperature: 0.8,
    });

    return new Response(JSON.stringify({ success: true, prompt: (response as any).response }), {
      headers: { 'Content-Type': 'application/json', ...CORS_HEADERS },
    });
  } catch (err: any) {
    return new Response(JSON.stringify({ success: false, error: err.message }), {
      status: 500,
      headers: { 'Content-Type': 'application/json', ...CORS_HEADERS },
    });
  }
};

export const onRequestOptions: PagesFunction = async () => {
  return new Response(null, { headers: CORS_HEADERS });
};
